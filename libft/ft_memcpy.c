#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t		i;

	i = 0;
	while (i < n && n)
	{
		*(unsigned char *)dst++ = *(unsigned char *)src++;
		i++;
	}
	return (dst - i);
}
