#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 1;
	if (s1 && s2)
	{
		while ((s1[i] || s2[i]))
		{
			if (s1[i] != s2[i])
				j = 0;
			i++;
		}
		return (j);
	}
	return (0);
}
